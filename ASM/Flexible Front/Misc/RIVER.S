;*************************************
;** ROUTINE FOR SCROLLER UPDATE     **
;*************************************
GETDOWN:
 LEA BADDS,A2
GPEK2:
 MOVEQ #32/2,D0
 BNE GG3
 MOVE.B #32/2,GPEK2+1
GPEK3:
 LEA BLOCKS,A0        ;PLACE NEW BLOCKS
 CMP.L #BLOCKSLUT,A0
 BNE GG4
 LEA BLOCKS,A0
GG4:
 MOVEQ #9,D1
 LEA BLOCKGX,A3
 MOVEQ #0,D7          ;OBST. X FACTOR --
 MOVEQ #5,D2
GG5:
 CLR.L D0
 MOVE.B (A0)+,D0
 BPL GG10

  MOVE.W D0,D3        ;INSERT CANNON
  NEG.B D3
  LSL.W #3,D3
  LEA CANNSPECS,A5
  ADD.W D3,A5
  MOVEQ #9,D4
  LEA CANNTAB-16,A4      ;FIND FREE
GG11:
  ADD.W #16,A4
  TST.W (A4)
  DBEQ D4,GG11
  TST.W D4
  BMI GG10               ;NONE FOUND
  MOVE.W (A5)+,(A4)+     ;HITS
  MOVE.W (A5)+,(A4)+     ;SHOTS
  MOVE.L (A5)+,(A4)+     ;GX
  MOVE.W #-(32*160),(A4)+ ;Y ADD
  MOVE.W D7,(A4)+        ;X COORD
  CLR.L (A4)+            ;NOT EXPLODED
  CLR.W D0

GG10:
 CMP.B #BLOCKHITNR,D0
 BLO GG7

  LEA OBSTXY-6,A4     ;INSERT OBSTACLE
  MOVEQ #13,D3
GG8:
  ADDQ.W #6,A4
  TST.W (A4)          ;FIND FREE
  DBEQ D3,GG8
  TST.W D3
  BMI GG7             ;NONE FOUND
  MOVE.W #-1,(A4)+    ;TAG ACTIVE
  MOVE.W #-(32*160),(A4)+  ;Y VALUE
  MOVE.W D7,(A4)+     ;X VALUE

GG7:
 LSL.L D1,D0
 LEA (A3,D0.L),A4
 MOVE.L A4,(A2)+
 ADDQ.W #8,D7
 DBRA D2,GG5

  CLR.W D0
  MOVE.B (A0)+,D0      ;ATTACK WAVE
  BEQ GG9
  CMP.B #2,D0
  BGE GG9
  TST.B WVFLAG
  BNE GG9
  LSL.W #2,D0
  LEA WAVEADDS,A4
  MOVE.L (A4,D0.W),WVPEK1+2
  ST WVFLAG

GG9:
 MOVE.L A0,GPEK3+2
 LEA BADDS,A2
GG3:
 SUBQ.B #1,GPEK2+1

GPEK1:
 LEA TEMP+(200*96),A1
 CMP.L #TEMP,A1
 BNE GG1
 MOVE.L #TEMP+(200*96),GPEK1+2
GG1:
 SUB.L #96*SCROLLSTEP,GPEK1+2   ;SCROLL

L@=0
N@=0
 REPEAT
 MOVE.L (A2),A0
 MOVEM.L (A0)+,D0-D7
 MOVEM.L D0-D3,96+L@(A1)
 MOVEM.L D0-D3,19296+L@(A1)
 MOVEM.L D4-D7,L@(A1)
 MOVEM.L D4-D7,19200+L@(A1)
 MOVE.L A0,(A2)+
N@=N@+1
L@=L@+16
 UNTILEQ N@,6

 MOVE.L A1,A0         ;MOVE DOWN TO SCREEN
 MOVE.L SCREENS,A1
L@=0
N@=0
 REPEAT
  MOVEM.L (A0)+,D0-D7/A2-A5
  MOVEM.L D0-D7/A2-A5,L@(A1)
  MOVEM.L (A0)+,D0-D7/A2-A5
  MOVEM.L D0-D7/A2-A5,L@+48(A1)
L@=L@+160
N@=N@+1
 UNTILEQ N@,200

 RTS
WAVEADDS:
 DC.L 0,WAVE1,WAVE2
