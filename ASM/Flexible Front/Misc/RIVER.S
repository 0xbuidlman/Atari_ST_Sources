;** END OF SCROLLER UPDATE ROUTINE **


;************************************
;** SHOTS UPDATE ROUTINE           **
;************************************
SHOTS:
 LEA SHOTLIST,A5
 MOVEQ #11,D7
SH1:
 BSR SHOT
 DBRA D7,SH1
 RTS
KILLSHOT:
 MOVE.W #0,-2(A5) ;FLAG: NOT ACTIVE
NEXTSHOT:
 ADDQ.W #4,A5
 RTS

SH2:              ;MOVE SHOT LEFT
 MOVE.B -2(A5),D0
 ROR.B #1,D0
 BCC.S SH5
 MOVE.B D0,-2(A5)
 BRA SH4
SH5:
 CMP.W #0,2(A5)      ;--
 BEQ KILLSHOT
 SUBQ.W #1,2(A5)
 MOVE.B D0,-2(A5)
 BRA SH4

SH3:              ;MOVE SHOT RIGHT
 MOVE.B -2(A5),D0
 ROR.B #1,D0
 BCC.S SH6
 MOVE.B D0,-2(A5)
 BRA SH4
SH6:
 CMP.W #44,2(A5)
 BGE KILLSHOT
 ADDQ.W #1,2(A5)
 MOVE.B D0,-2(A5)
 BRA SH4

SHOT:
 MOVE.W (A5)+,D0 ;MOVEMENT FACTOR
 BEQ NEXTSHOT
 SUB.W #14*160,(A5)
 TST.W (A5)
 BMI KILLSHOT
  CMP.B #2,D0
  BEQ SH2        ;LEFT
  CMP.B #3,D0
  BEQ SH3        ;RIGHT
SH4:
 MOVE.L SCREENS,A2
 ADD.W (A5)+,A2  ;Y COORD*160
 MOVE.W (A5)+,D0 ;X COORD
; SUBQ.W #4,D0    ;--
 MOVE.W D0,D1
 AND.B #%11111100,D0
 LSL.W #1,D0
 ADD.W D0,A2
 MOVE.L A2,A3
 AND.W #3,D1
 LSL.W #3,D1
 LEA SHOTGX,A0
 LEA SHOTADDS,A4
 ADD.W D1,A4
 ADD.W (A4)+,A2
 ADD.W (A4)+,A3
 ADD.W (A4)+,A0
 LEA 16(A0),A1

 MOVE.B (A0)+,D0
 MOVE.B (A0)+,D1
 AND.B D0,(A2)
 AND.B D0,2(A2)
 AND.B D0,4(A2)
 AND.B D0,6(A2)
 OR.B D1,6(A2)
 MOVE.B (A1)+,D0
 MOVE.B (A1)+,D1
 AND.B D0,(A3)
 AND.B D0,2(A3)
 AND.B D0,4(A3)
 AND.B D0,6(A3)
 OR.B D1,6(A3)
 MOVE.B (A0)+,D0
 MOVE.B (A0)+,D1
 MOVE.B (A1)+,D2
 MOVE.B (A1)+,D3
N@=0
L@=160
 REPEAT
  AND.B D0,L@(A2)
  AND.B D0,2+L@(A2)
  AND.B D0,4+L@(A2)
  AND.B D0,6+L@(A2)
  OR.B D1,6+L@(A2)
  AND.B D2,L@(A3)
  AND.B D2,2+L@(A3)
  AND.B D2,4+L@(A3)
  AND.B D2,6+L@(A3)
  OR.B D3,6+L@(A3)
L@=L@+160
N@=N@+1
 UNTILEQ N@,3
 RTS
SHOTADDS:
 DC.W 0,1,0,0, 0,1,32,0, 1,8,0,0, 1,8,32,0
