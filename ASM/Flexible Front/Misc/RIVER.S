  AND.L D7,D2
  OR.L (A2)+,D2
  AND.L D7,D3
  OR.L (A2)+,D3
  MOVEM.L D0-D3,L@(A1)
L@=L@+160
N@=N@+1
 UNTILEQ N@,16
 RTS
