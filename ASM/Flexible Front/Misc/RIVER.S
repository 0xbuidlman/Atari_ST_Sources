 BMI FIR1          ;NO FREE SHOTS FOUND
 MOVE.W #1,(A2)+   ;MOVEMENT FACTOR
 MOVE.W -2(A1),D0
 ADD.W #12*160,D0
 MOVE.W D0,(A2)    ;Y COORD*160
 MOVE.W (A1),D0
 ADDQ.W #1,D0
 MOVE.W D0,2(A2)   ;X COORD

 BSR FINDFREESHOT  ;RIGHT
