;***************************************
;** ROUTINE FOR SPRITE COLLISION      **
;***************************************
CHECKCOLLISIONS:
 BSR CHECKCOL1     ;HERO  VS OBSTACLES
 BSR CHECKCOL2     ;SHOTS VS 16*16 BADDIES
 BSR CHECKCOL3     ;SHOTS VS 32*32 BADDIES
 BSR CHECKCOL4     ;SHOTS VS CANNONS
 RTS

CHECKCOL1:         ;CHECK COLLISION: HERO VS GROUND OBSTACLES. UPDATE OBST Y.
 LEA SPRITELIST+12,A0
 MOVE.W (A0)+,D6   ;HERO Y
 MOVE.W (A0)+,D7   ;HERO X
 LEA OBSTXY,A0

 MOVEQ #13,D5
CHC1:
 MOVE.W (A0)+,D0
 BEQ CHC7          ;NOT ACTIVE
 MOVE.L (A0),D0    ;OBST XY
 SWAP D0
 MOVE.L D0,D1

 SUB.W D6,D1
 ADD.W #32*160,D1
 BMI.S CHC3        ;NO COLL
 CMP.W #48*160,D1
 BGE.S CHC3        ;NO COLL

 SWAP D1

 SUB.W D7,D1
 ADDQ.W #8,D1
 BMI.S CHC3        ;NO COLL
 CMP.W #12,D1
 BGE.S CHC3        ;NO COLL

 NOP
; MOVE.W #$700,$FF8240  ;COLLISION=TRUE
; MOVE.W #$000,$FF8240
CHC3:              ;COLLISION=FALSE
 CMP.W #190*160,D0
 BGE CHC8
 ADD.W #160*SCROLLSTEP,D0
 MOVE.W D0,(A0)

CHC7:
 ADDQ.W #4,A0
 DBRA D5,CHC1

 RTS
CHC8:              ;REMOVE
 MOVE.W #0,-2(A0)  ;NOT ACTIVE
 BRA.S CHC7


CHECKCOL2:         ;CHECK COLLISION: HERO SHOTS VS 16*16 BADDIES
 LEA SPRITELIST+16,A1
 MOVEQ #7,D4
CHC22:
 TST.L (A1)
 BEQ CHC24
   CMP.L #STYREX,(A1)
   BEQ CHC24
   MOVE.W 12(A1),D6 ;BAD Y
   MOVE.W 14(A1),D7 ;BAD X
   BRA.S CHC28
CHC29:
   TST.W D5
   BMI.S CHC24
     MOVE.L #STYREX,(A1) ;TRIGG AN EXPLOSION
     MOVE.L #EX16TAB,4(A1)
     MOVE.L #SPRITEGX+(12*384),8(A1)
CHC24:
 ADD.W #16,A1
 DBRA D4,CHC22
 RTS
CHC28:
 LEA SHOTLIST,A0
 MOVEQ #11,D5
CHC21:
 TST.W (A0)
 BEQ.S CHC27          ;NOT ACTIVE
 MOVE.L 2(A0),D1      ;SHOT XY
 SWAP D1

 SUB.W D6,D1
 ADD.W #4*160,D1
 BMI.S CHC27          ;NO COLL
 CMP.W #20*160,D1
 BGE.S CHC27          ;NO COLL

 SWAP D1

 SUB.W D7,D1
 ADDQ.W #2,D1
 BMI.S CHC27        ;NO COLL
 CMP.W #6,D1
 BGE.S CHC27        ;NO COLL
 MOVE.W #0,(A0)     ;COLLISION=TRUE. REMOVE SHOT
 BRA CHC29
CHC27:              ;COLLISION=FALSE
 ADDQ.W #6,A0
 DBRA D5,CHC21
 BRA CHC29


CHECKCOL3:          ;CHECK COLLISION: HERO SHOTS VS 32*32 BADDIES
 LEA SPRITELIST32,A1
 MOVEQ #3,D4
CHC32:
 TST.L (A1)
 BEQ.S CHC34
  CMP.L #STYREX,(A1)
  BEQ.S CHC34
   MOVE.W 12(A1),D6 ;BAD Y
   MOVE.W 14(A1),D7 ;BAD X
   BRA.S CHC38
CHC39:
   TST.W D5
   BMI.S CHC34
     SUBQ.W #1,16(A1)   ;SUFFICENT HITS?
     BPL.S CHC34        ;NO
       MOVE.L #STYREX,(A1) ;TRIGG AN EXPLOSION
       MOVE.L #EX32TAB,4(A1)
       MOVE.L #SPRITEGX32+(4*1152),8(A1)
CHC34:
 ADD.W #18,A1
 DBRA D4,CHC32
 RTS
CHC38:
 LEA SHOTLIST,A0
 MOVEQ #11,D5
CHC31:
 TST.W (A0)
 BEQ.S CHC37         ;NOT ACTIVE
 MOVE.L 2(A0),D1     ;SHOT XY
 SWAP D1

 SUB.W D6,D1
 ADD.W #4*160,D1    ;SHOT HEIGHT
 BMI.S CHC37        ;NO COLL
 CMP.W #36*160,D1   ;SHOT HEIGHT+SPRITE HEIGHT
 BGE.S CHC37        ;NO COLL

 SWAP D1

 SUB.W D7,D1
 ADDQ.W #2,D1       ;SHOT WIDTH
 BMI.S CHC37        ;NO COLL
 CMP.W #10,D1       ;SHOT WIDTH+SPRITE WIDTH
 BGE.S CHC37        ;NO COLL
 MOVE.W #0,(A0)     ;COLLISION=TRUE. REMOVE SHOT
 BRA CHC39
CHC37:              ;COLLISION=FALSE
 ADDQ.W #6,A0
 DBRA D5,CHC31
 BRA CHC39

CHECKCOL4:          ;CHECK COLLISION: HERO SHOTS VS CANNONS
 LEA CANNTAB,A1
 MOVEQ #9,D4
CHC42:
 TST.W (A1)
 BEQ.S CHC44
  TST.L 12(A1)      ;ALREADY EXPLODED?
  BNE.S CHC44
   MOVE.W 8(A1),D6  ;CANN Y
   MOVE.W 10(A1),D7 ;CANN X
   BRA.S CHC48
CHC49:
   TST.W D5
   BMI.S CHC44
     SUBQ.W #1,(A1)   ;SUFFICENT HITS?
     BNE.S CHC44      ;NO
       MOVE.L #EXCATAB,12(A1) ;TRIGG AN EXPLOSION
       MOVE.L #CANNONGX,4(A1)
       MOVE.W #$-1,(A1)       ;KEEP ACTIVE
CHC44:
 ADD.W #16,A1
 DBRA D4,CHC42
 RTS
CHC48:
 LEA SHOTLIST,A0
 MOVEQ #11,D5
CHC41:
 TST.W (A0)
 BEQ.S CHC47        ;NOT ACTIVE
 MOVE.L 2(A0),D1    ;SHOT XY
 SWAP D1

 SUB.W D6,D1
 ADD.W #4*160,D1    ;SHOT HEIGHT
 BMI.S CHC47        ;NO COLL
 CMP.W #36*160,D1   ;SHOT HEIGHT+SPRITE HEIGHT
 BGE.S CHC47        ;NO COLL

 SWAP D1

 SUB.W D7,D1
 ADDQ.W #2,D1       ;SHOT WIDTH
 BMI.S CHC47        ;NO COLL
 CMP.W #10,D1       ;SHOT WIDTH+SPRITE WIDTH
 BGE.S CHC47        ;NO COLL
 MOVE.W #0,(A0)     ;COLLISION=TRUE. REMOVE SHOT
 BRA CHC49
CHC47:              ;COLLISION=FALSE
 ADDQ.W #6,A0
 DBRA D5,CHC41
 BRA CHC49
