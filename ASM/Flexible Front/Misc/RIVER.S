WAVEUPDAT:
 TST.B WVFLAG
 BEQ WV1
 TST.W WVWAITCOUNT
 BEQ WVPEK1
   SUBQ.W #1,WVWAITCOUNT
   RTS
WVPEK1:    ;PEK!!!
 LEA WAVE1,A0
 MOVE.W (A0)+,D0   ;COMMAND
 BEQ WVEND
 CMP.B #1,D0
 BEQ WVI16
 CMP.B #2,D0
 BEQ WVI32
 CMP.B #3,D0
 BEQ WVWAIT
WV2:
 MOVE.L A0,WVPEK1+2
WV1:
 RTS

WVI16:             ;INSERT A 16*16 SPRITE
 LEA SPRITELIST,A1
 MOVEQ #16,D1
 MOVEQ #7,D0
WVI161:
 ADD.W D1,A1
 TST.L (A1)
 DBEQ D0,WVI161
 TST.W D0
 BMI WVI162
 MOVE.L (A0)+,(A1)+ ;STYRRUTIN
 MOVE.L (A0)+,(A1)+ ;MOVEMENT TABEL
 MOVE.L (A0)+,(A1)+ ;SPRITE GRAF
 MOVE.L #0,(A1)     ;Y X COORDS
 BRA WV2
WVI162:
 ADD.W #12,A0
 BRA WV2

WVI32:              ;INSERT A 32*32 SPRITE
 LEA SPRITELIST32-18,A1
 MOVEQ #18,D1
 MOVEQ #3,D0
WVI321:
