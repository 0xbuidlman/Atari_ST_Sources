FIRE:
 TST.B FIREFLAG
 BEQ FIR1
 CLR.B FIREFLAG
 BSR FINDFREESHOT
 TST.W D1
 BMI FIR1          ;NO FREE SHOTS FOUND
 MOVE.W #1,(A2)+   ;MOVEMENT FACTOR
 MOVE.W -2(A1),D0
