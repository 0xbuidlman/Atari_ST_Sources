 MOVE.L (A0)+,(A1)+ ;STYRRUTIN
 MOVE.L (A0)+,(A1)+ ;MOVEMENT TABEL
 MOVE.L (A0)+,(A1)+ ;SPRITE GRAF
 MOVE.L #$00000004,(A1)+    ;Y X COORDS
 MOVE.W (A0)+,(A1)  ;HITS
 BRA WV2
WVI322:
