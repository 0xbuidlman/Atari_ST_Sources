 BRA WV2

WVWAIT:            ;WAIT
 MOVE.W (A0)+,WVWAITCOUNT
 BRA WV2

WVEND:             ;END WAVE
 CLR.B WVFLAG
 RTS
WVWAITCOUNT:
 DC.W 0
WVFLAG:
 DC.W 0
