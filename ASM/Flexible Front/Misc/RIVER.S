 AND.B #%11111100,D0
 LSL.W #1,D0
 ADD.W D0,A1
 AND.W #3,D1
 LSL.W #1,D1
 LEA SPRADDS(PC),A3
 ADD.W (A3,D1.W),A2

L@=0
N@=0
