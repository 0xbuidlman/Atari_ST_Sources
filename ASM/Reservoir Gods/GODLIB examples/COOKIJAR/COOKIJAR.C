/*-----------------------------------------------------------------------------------*
* FUNCTION : main( void )
* ACTION   : it begins  ...
* CREATION : 18.02.2002 PNK
*-----------------------------------------------------------------------------------*/

S16	main( S16 aArgCount, char * apArgs[] )
{
	GemDos_Super( 0 );

	if( CookieJar_Exists() )
	{
		if( CookieJar_CookieExists( '_MCH' ) )
		{
			printf( "_MCH value : %lX\n", CookieJar_GetCookieValue( '_MCH' ) );
		}
		else
		{
			printf( "_MCH Cookie not found\n" );
		}
	}
	else
	{
		printf( "No Cookie Jar found\n" );
	}

	GemDos_Cnecin();

	return( 0 );
}




